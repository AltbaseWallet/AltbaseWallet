/* ───── registry ───── */

const dictionary: Record<Language, Partial<Record<TranslationKey, string>>> = { en, de, fr, zh, ja, ru, uk }

const interpolate = (template: string, vars?: Record<string, string | number>) => {
  if (!vars) return template
  return template.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? `{${k}}`))
}

/** Stateless translate — when you need a string outside React. */
export const translate = (
  language: Language,
  key: TranslationKey,
  vars?: Record<string, string | number>,
): string => interpolate(dictionary[language]?.[key] ?? dictionary.en[key] ?? key, vars)

/** React hook — returns a t() bound to the current language from settingsStore. */
export const useT = () => {
  const language = useSettingsStore((s) => s.settings.language)
  return useCallback(
    (key: TranslationKey, vars?: Record<string, string | number>) => translate(language, key, vars),
    [language],
  )
}

